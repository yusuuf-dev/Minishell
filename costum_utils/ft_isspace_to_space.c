#include "../minishell.h"

int	ft_isspace_to_space(char **s)
{
	size_t	i;

	i = 0;
	while ((*s)[i])
	{
		if ((*s)[i] != ' ' && !((*s)[i] <= 13 && (*s)[i] >= 9))
			return (0);
		i++;
	}
	return (1);
}
