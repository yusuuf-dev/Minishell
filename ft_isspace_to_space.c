#include "minishell.h"

void	ft_isspace_to_space(char *s)
{
	size_t	i;

	i = 0;

	while (s[i])
	{
		if ((s[i] <= 13 && s[i] >= 11) || s[i] == 9)
			s[i] = ' ';
		i++;
	}
}
